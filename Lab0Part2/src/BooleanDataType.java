
public class BooleanDataType {

	public static void main(String[] args) {
		
		// The boolean data type consists of 
		// only two data types: true and false.
		// true and false are the only two 
		// possible boolean literals.
		boolean p = true;
		boolean q = false;
		System.out.println("p is " + p);
		System.out.println("q is " + q);
		
		System.out.println("After reassigning p to false, and q to true.");
		p = false;
		q = true;
		System.out.println("p is " + p);
		System.out.println("q is " + q);
		
	}

}
